    <h1>🌱 À propos de la DD&RS à Centrale Lille Institut</h1>

    <p>
        Le Développement Durable et la Responsabilité Sociétale (DD&RS) font partie intégrante des valeurs portées par
        notre établissement. Notre engagement vise à former des citoyennes et citoyens responsables, conscients des
        enjeux environnementaux, sociaux et éthiques de leur époque.
    </p>

    <h2>🎯 Nos objectifs</h2>

    <p>
        Intégrer les principes du développement durable dans nos formations, projets et pratiques.<br>
        Sensibiliser l’ensemble de la communauté (étudiants, enseignants, personnels, associations) aux enjeux de
        transition écologique, d’égalité, d’inclusion et de sobriété.<br>
        Soutenir les initiatives étudiantes et associatives qui s’inscrivent dans cette démarche.<br>
        Réduire l’empreinte environnementale de notre campus.
    </p>

    <h2>📜 Nos engagements</h2>

    <p>
        Adoption d’une charte DD&RS alignée avec les Objectifs de Développement Durable (ODD) de l’ONU.<br>
        Mise en œuvre d’un plan d’action DD&RS à l’échelle de l’institut.<br>
        Collaboration active avec des acteurs publics et associatifs engagés.
    </p>

    <h2>🤝 Comment vous impliquer ?</h2>

    <p>
        Participez aux événements proposés dans l’application (ateliers, conférences, campagnes, défis…)<br>
        Proposez vos idées via vos associations ou directement via l’équipe DD&RS.<br>
        Suivez vos engagements et découvrez les badges Climpact qui valorisent vos actions responsables !
    </p>

